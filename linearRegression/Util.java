    public static double[] flatten(double[][] arr) {
      var res = new double[arr.length];
     for (int i = 0; i < res.length; i++)
       res[i] = arr[i][0];


     return res;

    }


    public static double[][][] train_test_split(double[][] X, double[] Y) {
        double[][][] res = new double[4][][];
        int recordSize = X.length > 0 ? X[0].length : 0;

        // X train
        res[0] = new double[X.length / 2][recordSize];
        // Y train
        res[1] = new double[Y.length / 2][1];
        // X test
        res[2] = new double[X.length / 2][recordSize];
        // Y test
        res[3] = new double[Y.length/2][1];

        for (int i = 0; i < X.length / 2; i++) {
            res[0][i] = X[i];
            res[1][i][0] = Y[i];
            res[2][i] = X[i + X.length / 2];
            res[3][i][0] = Y[i + X.length / 2];
        }

        return res;
    }


